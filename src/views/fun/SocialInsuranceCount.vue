<script lang="ts" setup>
import { ref, computed, onMounted, onUnmounted } from "vue";
import CardView from "@/components/CardView.vue";

// 定义保险项目接口
interface InsuranceItem {
  name: string;
  enabled: boolean;
  personalRate: number;
  companyRate: number;
}

// 定义税率表项目接口
interface TaxBracket {
  limit: number;
  rate: number;
  deduction: number;
  name: string;
}

// 工资组成部分
const baseSalary = ref(10000); // 底薪
const commission = ref(3000); // 提成
const allowance = ref(2000); // 津贴补贴
const bonus = ref(0); // 奖金

// 计算总工资
const salary = computed(() => {
  const base = baseSalary.value || 0;
  const comm = commission.value || 0;
  const allow = allowance.value || 0;
  const bon = bonus.value || 0;
  return base + comm + allow + bon;
});

const baseMin = ref(4000); // 社保基数下限
const baseMax = ref(25000); // 社保基数上限

// 默认社保项目配置
const defaultInsuranceItems: InsuranceItem[] = [
  { name: "养老保险", enabled: true, personalRate: 8, companyRate: 16 },
  { name: "医疗保险", enabled: true, personalRate: 2, companyRate: 10 },
  { name: "失业保险", enabled: true, personalRate: 0.5, companyRate: 0.5 },
  { name: "工伤保险", enabled: true, personalRate: 0, companyRate: 0.2 },
  { name: "生育保险", enabled: true, personalRate: 0, companyRate: 0.8 },
  { name: "住房公积金", enabled: true, personalRate: 12, companyRate: 12 },
];

const insuranceItems = ref<InsuranceItem[]>(JSON.parse(JSON.stringify(defaultInsuranceItems)));

// 个人所得税税率表（2024年标准），允许用户修改
const defaultTaxBrackets: TaxBracket[] = [
  { limit: 36000, rate: 3, deduction: 0, name: "3万6以下" },
  { limit: 144000, rate: 10, deduction: 2520, name: "3万6-14万4" },
  { limit: 300000, rate: 20, deduction: 16920, name: "14万4-30万" },
  { limit: 420000, rate: 25, deduction: 31920, name: "30万-42万" },
  { limit: 660000, rate: 30, deduction: 52920, name: "42万-66万" },
  { limit: 960000, rate: 35, deduction: 85920, name: "66万-96万" },
  { limit: Infinity, rate: 45, deduction: 181920, name: "96万以上" },
];

const taxBrackets = ref<TaxBracket[]>(JSON.parse(JSON.stringify(defaultTaxBrackets)));

// 检测当前主题
const isDarkMode = computed(() => {
  if (typeof document !== 'undefined') {
    return document.documentElement.getAttribute("data-theme") === "dark";
  }
  return false;
});

// 切换明暗主题
const toggleTheme = () => {
  if (typeof document !== 'undefined') {
    const currentTheme = document.documentElement.getAttribute("data-theme") || "light";
    const newTheme = currentTheme === "dark" ? "light" : "dark";
    document.documentElement.setAttribute("data-theme", newTheme);
    localStorage.setItem("theme", newTheme);
  }
};

// 打开链接
const openLink = (url: string) => {
  window.open(url, '_blank');
};

// Tooltip状态管理
const activeTooltip = ref<string | null>(null);

// 定义tooltip内容
const tooltips = {
  socialSecurityBase: {
    title: '社保基数说明',
    content: '社保基数是计算社保缴费的基础，通常为当地平均工资的60%-300%之间。各地标准不同，请查看具体政策。',
    link: 'http://www.gov.cn/fuwu/2024-06/28/content_6954329.htm'
  },
  insuranceItems: {
    title: '社保项目说明',
    content: '包括养老保险、医疗保险、失业保险、工伤保险、生育保险和住房公积金。各项比例根据国家和地方政策确定。',
    link: 'http://www.gov.cn/zhengce/xxgk/main/202312/content_6920663.htm'
  },
  taxBrackets: {
    title: '个人所得税说明',
    content: '按照7级超额累进税率计算，起征点为5000元/月。税率从3%到45%不等。',
    link: 'http://www.gov.cn/zhengce/2018-08/31/content_5318023.htm'
  }
};

// 显示tooltip
const showTooltip = (tooltipKey: string) => {
  activeTooltip.value = tooltipKey;
};

// 隐藏tooltip
const hideTooltip = () => {
  activeTooltip.value = null;
};

// 点击tooltip内容时跳转链接
const handleTooltipClick = (tooltipKey: string) => {
  const tooltip = tooltips[tooltipKey as keyof typeof tooltips];
  if (tooltip && tooltip.link) {
    openLink(tooltip.link);
  }
  hideTooltip();
};

// 点击外部区域关闭tooltip
const handleClickOutside = (event: Event) => {
  const target = event.target as HTMLElement;
  if (activeTooltip.value && !target.closest('.tooltip-container')) {
    hideTooltip();
  }
};

// 在组件挂载时添加全局点击监听
onMounted(() => {
  document.addEventListener('click', handleClickOutside);
});

// 在组件卸载时移除监听器
onUnmounted(() => {
  document.removeEventListener('click', handleClickOutside);
});

// 计算结果
const results = computed(() => {
  const monthlySalary = salary.value || 0;
  const minBase = baseMin.value || 0;
  const maxBase = baseMax.value || 0;
  let personalTotal = 0;
  let companyTotal = 0;

  // 计算社保基数（在基数范围内）
  const socialSecurityBase = Math.max(minBase, Math.min(monthlySalary, maxBase));

  const itemCalculations = insuranceItems.value.map((item: InsuranceItem) => {
    if (!item.enabled) {
      return { name: item.name, personal: 0, company: 0 };
    }
    
    const personalRate = item.personalRate || 0;
    const companyRate = item.companyRate || 0;
    const personal = (socialSecurityBase * personalRate) / 100;
    const company = (socialSecurityBase * companyRate) / 100;
    
    // 确保计算结果是有效数字
    const personalAmount = isNaN(personal) ? 0 : personal;
    const companyAmount = isNaN(company) ? 0 : company;
    
    personalTotal += personalAmount;
    companyTotal += companyAmount;
    
    return { name: item.name, personal: personalAmount, company: companyAmount };
  });

  // 计算个人所得税
  const taxableIncome = Math.max(0, monthlySalary - personalTotal - 5000); // 减去个人缴纳和基本免征额
  const annualTaxableIncome = taxableIncome * 12;

  let tax = 0;
  for (const bracket of taxBrackets.value) {
    const rate = bracket.rate || 0;
    const deduction = bracket.deduction || 0;
    const limit = bracket.limit || 0;
    
    if (annualTaxableIncome <= limit) {
      tax = (annualTaxableIncome * (rate / 100) - deduction) / 12;
      break;
    }
  }
  
  // 确保税额不为负数且是有效数字
  tax = Math.max(0, isNaN(tax) ? 0 : tax);

  // 计算实际到手工资，确保不为负数
  const takeHomePay = Math.max(0, monthlySalary - personalTotal - tax);
  
  // 检测是否存在工资过低的情况
  const isLowSalaryWarning = monthlySalary > 0 && (monthlySalary - personalTotal - tax) < 0;

  return {
    socialSecurityBase: isNaN(socialSecurityBase) ? 0 : socialSecurityBase,
    personalTotal: isNaN(personalTotal) ? 0 : personalTotal,
    companyTotal: isNaN(companyTotal) ? 0 : companyTotal,
    tax,
    takeHomePay: isNaN(takeHomePay) ? 0 : takeHomePay,
    itemCalculations,
    isLowSalaryWarning,
  };
});

// 重置为默认值
const resetToDefault = () => {
  baseSalary.value = 10000;
  commission.value = 3000;
  allowance.value = 2000;
  bonus.value = 0;
  baseMin.value = 4000;
  baseMax.value = 25000;
  insuranceItems.value = JSON.parse(JSON.stringify(defaultInsuranceItems));
  taxBrackets.value = JSON.parse(JSON.stringify(defaultTaxBrackets));
};
</script>

